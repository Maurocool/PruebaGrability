
package com.grability.DataObject;

/**
 * Objeto Entry
 * @author ASUS
 *
 */
public class Entry {
	
    /**
     * Nombre de la aplicacion
     */
    private String imName;
    /**
     * Descripcion de la Aplicacion
     */
    private String summary;
    /**
     * Precio de la aplicacion
     */
    private String imPrice;
    /**
     * Tipo monda de la aplicacion
     */
    private String currency;
    /**
     * Tipo de la aplicacion
     */
    private String imContentType;
    /**
     * Derechos de la aplicacion
     */
    private String rights;
    /**
     * Titulo de la aplicacion
     */
    private String title;
    /**
     * Link informacion de la aplicacion
     */
    private String link;
    /**
     * Idnetificador de la aplicacion
     */
    private String id;
    /**
     * Informacion artista de la aplicacion
     */
    private String imArtist;
    /**
     * Categoria de la aplicacion
     */
    private String category;
    /**
     * Identificador de la categoria de la aplicacion
     */
    private String idCategory;
    /**
     * Fecha de publicacion
     */
    private String imReleaseDate;
    /**
     * Url de la imagen de la aplicacion
     */
    private String urlImagen;
    
    
	public String getImName() {
		return imName;
	}
	public void setImName(String imName) {
		this.imName = imName;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getImPrice() {
		return imPrice;
	}
	public void setImPrice(String imPrice) {
		this.imPrice = imPrice;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getImContentType() {
		return imContentType;
	}
	public void setImContentType(String imContentType) {
		this.imContentType = imContentType;
	}
	public String getRights() {
		return rights;
	}
	public void setRights(String rights) {
		this.rights = rights;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getImArtist() {
		return imArtist;
	}
	public void setImArtist(String imArtist) {
		this.imArtist = imArtist;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	public String getIdCategory() {
		return idCategory;
	}
	public void setIdCategory(String idCategory) {
		this.idCategory = idCategory;
	}
	public String getImReleaseDate() {
		return imReleaseDate;
	}
	public void setImReleaseDate(String imReleaseDate) {
		this.imReleaseDate = imReleaseDate;
	}
	public String getUrlImagen() {
		return urlImagen;
	}
	public void setUrlImagen(String urlImagen) {
		this.urlImagen = urlImagen;
	}
}
